public class CobaOperator {
    public static void main(String[] args) {
        int max;
        int min = 10;
        max = 100 - 4 / 2;
        max = max + 8;
        min = max - min;
        System.out.println(max * 5);
        System.out.println(max + min);
        System.out.println(max);
        System.out.println(min);
        System.out.println();
    }
}